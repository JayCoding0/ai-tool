#!/bin/bash
# ============================================================
# AI Agent Platform - 重启脚本
# 用法: ./scripts/restart.sh [--dev|--prod] [--port PORT]
# ============================================================

set -e

# ─── 颜色定义 ───────────────────────────────────────────────
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m'

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"

# ─── 工具函数 ───────────────────────────────────────────────
log_info()  { echo -e "${GREEN}[INFO]${NC}  $1"; }

# ─── 主流程 ─────────────────────────────────────────────────
echo -e "${BLUE}╔══════════════════════════════════════╗${NC}"
echo -e "${BLUE}║   🤖 AI Agent Platform - 重启中     ║${NC}"
echo -e "${BLUE}╚══════════════════════════════════════╝${NC}"
echo ""

# 第一步：停止服务
log_info "第 1/2 步：停止现有服务..."
echo ""
"$SCRIPT_DIR/stop.sh"

echo ""
log_info "等待 2 秒确保端口释放..."
sleep 2

# 第二步：启动服务（透传所有参数）
echo ""
log_info "第 2/2 步：启动服务..."
echo ""
"$SCRIPT_DIR/start.sh" "$@"
